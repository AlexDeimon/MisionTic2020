//1) Declara dos variables numéricas (con el valor que desees), 
//muestra por consola la suma, resta, multiplicación, división y módulo (resto de la división).
package ejercicios;

public class Ejercicio01 {
    public static void main(String[] args) {
        int numero1=31;
        int numero2=12;
        System.out.println("suma entre "+numero1+" y "+numero2+" es igual a "+(numero1+numero2));
        System.out.println("resta entre "+numero1+" y "+numero2+" es igual a "+(numero1-numero2));
        System.out.println("multiplicación entre "+numero1+" y "+numero2+" es igual a "+(numero1*numero2));
        System.out.println("división entre "+numero1+" y "+numero2+" es igual a "+(numero1/numero2));
        System.out.println("módulo entre "+numero1+" y "+numero2+" es igual a "+(numero1%numero2));
    }
    
}
